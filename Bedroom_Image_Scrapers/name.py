import torch
import clip
from PIL import Image

device = "cuda" if torch.cuda.is_available() else "cpu"
model, preprocess = clip.load("ViT-B/32", device=device)

image = preprocess(Image.open("female.jpg")).unsqueeze(0).to(device)
text = clip.tokenize(["less bushy tail","small heads and snouts","shorter and less pointed ears"]).to(device)
#["pointed and taller ears","bushy tail","broader heads and snouts"]
with torch.no_grad():
    image_features = model.encode_image(image)
    text_features = model.encode_text(text)
    
    logits_per_image, logits_per_text = model(image, text)
    probs = logits_per_image.softmax(dim=-1).cpu().numpy()

print("Label probs:", probs)  # prints: [[0.9927937  0.00421068 0.00299572]]so